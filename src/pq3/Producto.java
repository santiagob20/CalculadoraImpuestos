package pq3;

/**
 *
 * @author santiagob20
 */
public class Producto {
    private String nombre;
    private int valorP;
    private String categoria;
    private int valorImp;
    private int total;

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the valorP
     */
    public int getValorP() {
        return valorP;
    }

    /**
     * @param valorP the valorP to set
     */
    public void setValorP(int valorP) {
        this.valorP = valorP;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the valorImp
     */
    public int getValorImp() {
        return valorImp;
    }

    /**
     * @param valorImp the valorImp to set
     */
    public void setValorImp(int valorImp) {
        this.valorImp = valorImp;
    }

    /**
     * @return the total
     */
    public int getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(int total) {
        this.total = total;
    }
    
    
}
