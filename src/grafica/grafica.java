    private void seleccionarImp(String seleccion){
        
            switch(seleccion){
                case "19% Tecnología":
                    precio = Integer.parseInt(txtValorP.getText());
                    impuesto = (int) (precio*0.19);
                    total = precio+impuesto;
                    txtImp.setText(String.valueOf(impuesto));
                    txtTotal.setText(String.valueOf(total));
                    break;

                case "20% Finca raiz":
                    precio = Integer.parseInt(txtValorP.getText());
                    impuesto = (int) (precio*0.20);
                    total = precio+impuesto;
                    txtImp.setText(String.valueOf(impuesto));
                    txtTotal.setText(String.valueOf(total));
                    break;

                case "15% Canasta familiar":
                    precio = Integer.parseInt(txtValorP.getText());
                    impuesto = (int) (precio*0.15);
                    total = precio+impuesto;
                    txtImp.setText(String.valueOf(impuesto));
                    txtTotal.setText(String.valueOf(total));
                    break;

                case "9% Otro":
                    precio = Integer.parseInt(txtValorP.getText());
                    impuesto = (int) (precio*0.09);
                    total = precio+impuesto;
                    txtImp.setText(String.valueOf(impuesto));
                    txtTotal.setText(String.valueOf(total));
                    break;

                case "Seleccione...":
                    txtImp.setText("");
                    txtTotal.setText("");
            }
        }
    
    private void guardarProducto(){
        
        p.setNombre(txtNombre.getText());
        p.setValorP(Integer.parseInt(txtValorP.getText()));
        p.setCategoria(cmbCategoria.getSelectedItem().toString());
        p.setValorImp(Integer.parseInt(txtImp.getText()));
        p.setTotal(Integer.parseInt(txtTotal.getText()));
    }
    private void borrarCampos(){
        txtNombre.setText("");
        txtValorP.setText("");
        txtImp.setText("");
        txtTotal.setText("");
        cmbCategoria.setSelectedIndex(0);
    }
