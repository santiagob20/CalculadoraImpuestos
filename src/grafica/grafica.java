    private void txtValorPKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorPKeyTyped
        char c=evt.getKeyChar(); 
          if(!Character.isDigit(c)&& c!=KeyEvent.VK_BACK_SPACE){ 
              getToolkit().beep(); 
              evt.consume(); 
              JOptionPane.showMessageDialog(null,"Error, Solo puede ingresar numeros",null,JOptionPane.ERROR_MESSAGE);
          }    
    }//GEN-LAST:event_txtValorPKeyTyped

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        impProductos imp = new impProductos();
        
        if (txtNombre.getText().isEmpty()||txtValorP.getText().isEmpty()||txtImp.getText().isEmpty()||txtTotal.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos", "¡Error!", JOptionPane.ERROR_MESSAGE);    
        }
        guardarProducto();
        imp.insertar(p);
        borrarCampos();
    }//GEN-LAST:event_btnGuardarActionPerformed

